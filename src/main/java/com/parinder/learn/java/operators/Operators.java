package com.parinder.learn.java.operators;

/*
Java Operators or just operators perform an operation on a variable or value.
+,-,/ and * are four common ones that I feel sure you are familiar with.
 */
public class Operators {
    public static void main(String[] args) {
        int myFirstVariable = (10 - 2);
        int mySecondVariable = (10 + 2);
        int myThirdVariable = (10 / 2);
        int myForthVariable = (10 * 2);

        System.out.println(myFirstVariable);
        System.out.println(mySecondVariable);
        System.out.println(myThirdVariable);
        System.out.println(myForthVariable);
    }
}
